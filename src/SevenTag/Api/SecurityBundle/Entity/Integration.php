namespace SevenTag\Api\SecurityBundle\Entity;

use FOS\UserBundle\Model\UserInterface;
use OAuth2\OAuth2;
use SevenTag\Component\Integration\Model\IntegrationInterface;

class Integration extends Client implements IntegrationInterface
{
    /**
     * @var string
     */
    protected $name;

    /**
     * @var UserInterface
     */
    protected $user;

    public function __construct()
    {
        parent::__construct();

        $this->allowedGrantTypes = [
            OAuth2::GRANT_TYPE_CLIENT_CREDENTIALS,
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * {@inheritdoc}
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return UserInterface
     */
    public function getUser()
    {
        return $this->user;
    }

    /**
     * @param UserInterface $user
     */
    public function setUser(UserInterface $user)
    {
        $this->user = $user;
    }
}
